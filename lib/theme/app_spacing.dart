/// 应用全局间距常量
/// 统一定义内边距、外边距和常用尺寸阶梯
class AppSpacing {
  AppSpacing._();

  // ======================== 间距值 ========================

  /// 极小间距（2px）
  static const double xxs = 2.0;

  /// 很小间距（4px）
  static const double xs = 4.0;

  /// 小间距（8px）
  static const double sm = 8.0;

  /// 中小间距（10px）
  static const double ms = 10.0;

  /// 中间距（12px）
  static const double md = 12.0;

  /// 中大间距（14px）
  static const double ml = 14.0;

  /// 大间距（16px）
  static const double lg = 16.0;

  /// 超大间距（20px）
  static const double xl = 20.0;

  /// 特大间距（24px）
  static const double xxl = 24.0;

  /// 巨大间距（32px）
  static const double xxxl = 32.0;

  /// 页面边距（16px，桌面端可用 24px）
  static const double pagePadding = 16.0;

  /// 组件内部间距
  static const double widgetPadding = 12.0;

  /// 卡片内间距
  static const double cardPadding = 16.0;

  /// 列表项高度
  static const double listItemHeight = 48.0;

  /// 工具栏高度
  static const double toolbarHeight = 44.0;

  /// 状态栏高度
  static const double statusBarHeight = 32.0;

  /// 标签栏高度
  static const double tabBarHeight = 40.0;

  /// 最小触摸目标
  static const double minTapTarget = 44.0;

  // ======================== 间距常量集合 ========================

  /// 统一内边距（上下，左右）
  static const double inputVerticalPadding = 10.0;
  static const double inputHorizontalPadding = 12.0;
  static const double buttonVerticalPadding = 10.0;
  static const double buttonHorizontalPadding = 20.0;
  static const double cardVerticalPadding = 12.0;
  static const double cardHorizontalPadding = 16.0;

  /// 标签间距
  static const double tabBarPadding = 4.0;

  /// 工具栏间距
  static const double toolbarPadding = 8.0;

  /// 文件列表行内间距
  static const double fileListRowPadding = 8.0;

  /// 终端内边距
  static const double terminalPadding = 12.0;
}
